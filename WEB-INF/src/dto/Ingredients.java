package dto;

/**
 * Ingredients
 */
public class Ingredients {

    int id, prix;
    String name;

    
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getPrix() {
        return prix;
    }
    public void setPrix(int prix) {
        this.prix = prix;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}